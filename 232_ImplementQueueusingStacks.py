class MyQueue:
    # stack 2개를 이용하여 큐 구현
    # [2,3,1,4] 라고 생각
    def __init__(self):
        self.input = []
        self.output = []

    def push(self, x: int) -> None:
        self.input.append(x)

    def pop(self) -> int:
        self.peek()
        return self.output.pop()

    def peek(self) -> int:
        if not self.output:
            while self.input:
                self.output.append(self.input.pop())
        return self.output[-1]  # 뒤집으면서 해결

    def empty(self) -> bool:
        return self.input == [] and self.output == []


